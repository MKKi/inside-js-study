// create object using Object() constructor
var foo = new Object();

foo.name = 'foo';
foo.age = 30;
foo.gender = 'male';

console.log(typeof foo);
console.log(foo);

// create object using object literal

var foo2 = {
    name: 'foo',
    age: 30,
    gender: 'male'
};

console.log(typeof foo2);
console.log(foo2);